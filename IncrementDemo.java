public class IncrementDemo
{
   public static void main(String[] args)
   {
      int x = 10;
      int y = -3;
      // increment x
      x++;
      // display the vaue of x
      System.out.println("Value of x increment " + x);
      
      // increment y
      y++;
      // display the value of y
      System.out.println("Value of y increment " + y);
   }
}
